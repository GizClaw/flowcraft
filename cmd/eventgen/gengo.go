package main

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

func writeGoOutputs(spec *Spec, outDir string) error {
	if err := os.MkdirAll(outDir, 0o755); err != nil {
		return err
	}
	if err := writeGoEvents(spec, filepath.Join(outDir, "events_gen.go")); err != nil {
		return err
	}
	if err := writeGoPayloads(spec, filepath.Join(outDir, "payloads_gen.go")); err != nil {
		return err
	}
	if err := writeGoPartitions(spec, filepath.Join(outDir, "partitions_gen.go")); err != nil {
		return err
	}
	if err := writeGoDispatch(spec, filepath.Join(outDir, "dispatch_gen.go")); err != nil {
		return err
	}
	if err := writeGoPublish(spec, filepath.Join(outDir, "publish_gen.go")); err != nil {
		return err
	}
	return writeGoAudit(spec, filepath.Join(outDir, "audit_gen.go"))
}

func writeFile(path string, b []byte) error {
	hdr := []byte("// Code generated by eventgen. DO NOT EDIT.\n\n")
	return os.WriteFile(path, append(hdr, b...), 0o644)
}

func eventGoName(event string) string {
	var b strings.Builder
	for _, part := range strings.Split(event, ".") {
		if part == "" {
			continue
		}
		for _, sub := range strings.Split(part, "_") {
			if sub == "" {
				continue
			}
			b.WriteString(strings.ToUpper(sub[:1]) + sub[1:])
		}
	}
	return b.String()
}

// commonInitialisms is the set of all-caps acronyms used in Go field names.
// Aligned with the golint convention so generated structs match what humans
// would write by hand.
var commonInitialisms = map[string]string{
	"id":   "ID",
	"url":  "URL",
	"uri":  "URI",
	"api":  "API",
	"http": "HTTP",
	"json": "JSON",
	"sql":  "SQL",
	"ws":   "WS",
	"sse":  "SSE",
	"ip":   "IP",
	"db":   "DB",
	"ttl":  "TTL",
	"hmac": "HMAC",
	"cdn":  "CDN",
	"acl":  "ACL",
	"rpc":  "RPC",
}

func goFieldName(yamlKey string) string {
	parts := strings.Split(yamlKey, "_")
	var b strings.Builder
	for _, p := range parts {
		if p == "" {
			continue
		}
		if up, ok := commonInitialisms[strings.ToLower(p)]; ok {
			b.WriteString(up)
			continue
		}
		b.WriteString(strings.ToUpper(p[:1]) + p[1:])
	}
	return b.String()
}

func goTypeForField(f FieldDef) string {
	switch f.Type {
	case "string":
		return "string"
	case "int64":
		return "int64"
	case "int32":
		return "int32"
	case "bool":
		return "bool"
	case "float64":
		return "float64"
	case "timestamp":
		return "string"
	case "bytes":
		return "[]byte"
	case "any":
		return "any"
	case "array":
		it := "string"
		if f.ItemType != "" {
			it = goScalar(f.ItemType)
		}
		return "[]" + it
	case "map":
		vt := "any"
		if f.ValueType == "string" {
			vt = "string"
		} else if f.ValueType == "any" {
			vt = "any"
		}
		return "map[string]" + vt
	default:
		return "any"
	}
}

func goScalar(t string) string {
	switch t {
	case "string":
		return "string"
	case "int64":
		return "int64"
	case "int32":
		return "int32"
	case "bool":
		return "bool"
	default:
		return "string"
	}
}

func partitionArgName(p PartitionDef) string {
	switch p.Name {
	case "runtime":
		return "runtimeID"
	case "card":
		return "cardID"
	case "webhook_endpoint":
		return "endpointID"
	case "cron_rule":
		return "ruleID"
	case "realm":
		return "realmID"
	case "actor":
		return "actorID"
	default:
		return "id"
	}
}

func partitionFuncName(p PartitionDef) string {
	switch p.Name {
	case "runtime":
		return "Runtime"
	case "card":
		return "Card"
	case "webhook_endpoint":
		return "Webhook"
	case "cron_rule":
		return "Cron"
	case "realm":
		return "Realm"
	case "actor":
		return "Actor"
	default:
		return strings.ReplaceAll(strings.Title(strings.ReplaceAll(p.Name, "_", " ")), " ", "")
	}
}

func extractFormatPrefix(format string) (string, error) {
	i := strings.Index(format, "{")
	if i <= 0 {
		return "", fmt.Errorf("bad partition format %q", format)
	}
	return format[:i], nil
}

func categoryConstName(cat string) string {
	var b strings.Builder
	for _, part := range strings.Split(cat, "_") {
		if part == "" {
			continue
		}
		b.WriteString(strings.ToUpper(part[:1]) + part[1:])
	}
	return "Category" + b.String()
}

func writeGoEvents(spec *Spec, path string) error {
	var sb strings.Builder
	sb.WriteString("package eventlog\n\nconst (\n")
	for _, ev := range spec.Events {
		sb.WriteString(fmt.Sprintf("\tEventType%s = %q\n", eventGoName(ev.Name), ev.Name))
	}
	sb.WriteString(")\n\n// EventTypes lists every known envelope.type value (sorted).\nvar EventTypes = []string{\n")
	for _, ev := range spec.Events {
		sb.WriteString(fmt.Sprintf("\tEventType%s,\n", eventGoName(ev.Name)))
	}
	sb.WriteString("}\n")
	cats := make([]string, 0, len(spec.Categories))
	for c := range spec.Categories {
		cats = append(cats, c)
	}
	sort.Strings(cats)
	for _, cat := range cats {
		sb.WriteString(fmt.Sprintf("\nconst %s = %q\n", categoryConstName(cat), cat))
	}
	return writeFile(path, []byte(sb.String()))
}

func writeGoPayloads(spec *Spec, path string) error {
	var sb strings.Builder
	sb.WriteString("package eventlog\n\nimport \"encoding/json\"\n\n")
	names := make([]string, 0, len(spec.Payloads))
	for n := range spec.Payloads {
		names = append(names, n)
	}
	sort.Strings(names)
	for _, n := range names {
		p := spec.Payloads[n]
		sb.WriteString(fmt.Sprintf("// %s is generated from contracts.\ntype %s struct {\n", n, n))
		keys := make([]string, 0, len(p.Fields))
		for k := range p.Fields {
			keys = append(keys, k)
		}
		sort.Strings(keys)
		for _, k := range keys {
			f := p.Fields[k]
			sb.WriteString(fmt.Sprintf("\t%s %s `json:\"%s\"`\n", goFieldName(k), goTypeForField(f), k))
		}
		sb.WriteString("}\n\n")
	}
	sb.WriteString("// PayloadTypeFor returns the concrete payload struct name for an event type, or \"\" if unknown.\nfunc PayloadTypeFor(eventType string) string {\n\tswitch eventType {\n")
	for _, ev := range spec.Events {
		sb.WriteString(fmt.Sprintf("\tcase EventType%s:\n\t\treturn %q\n", eventGoName(ev.Name), ev.PayloadType))
	}
	sb.WriteString("\tdefault:\n\t\treturn \"\"\n\t}\n}\n\n")
	sb.WriteString("// UnmarshalPayload decodes env.Payload into the correct struct for env.Type.\nfunc UnmarshalPayload(env Envelope) (any, error) {\n\tswitch env.Type {\n")
	for _, ev := range spec.Events {
		g := eventGoName(ev.Name)
		sb.WriteString(fmt.Sprintf("\tcase EventType%s:\n\t\tvar p %s\n\t\tif err := json.Unmarshal(env.Payload, &p); err != nil {\n\t\t\treturn nil, err\n\t\t}\n\t\treturn &p, nil\n", g, ev.PayloadType))
	}
	sb.WriteString("\tdefault:\n\t\treturn nil, nil\n\t}\n}\n")
	return writeFile(path, []byte(sb.String()))
}

func writeGoPartitions(spec *Spec, path string) error {
	var sb strings.Builder
	sb.WriteString("package eventlog\n\nimport \"strings\"\n\n")
	for _, p := range spec.Partitions {
		pfx, err := extractFormatPrefix(p.Format)
		if err != nil {
			return err
		}
		fn := partitionFuncName(p)
		arg := partitionArgName(p)
		sb.WriteString(fmt.Sprintf("// Partition%s builds %q partitions.\nfunc Partition%s(%s string) string {\n\treturn %q + strings.TrimSpace(%s)\n}\n\n", fn, p.Name, fn, arg, pfx, arg))
	}
	return writeFile(path, []byte(sb.String()))
}

func writeGoDispatch(spec *Spec, path string) error {
	var sb strings.Builder
	sb.WriteString("package eventlog\n\nimport (\n\t\"context\"\n\t\"encoding/json\"\n\t\"fmt\"\n)\n\n")
	sb.WriteString("// Handler receives decoded envelope payloads. Embed DefaultHandler to opt in incrementally.\ntype Handler interface {\n")
	for _, ev := range spec.Events {
		g := eventGoName(ev.Name)
		sb.WriteString(fmt.Sprintf("\tHandle%s(ctx context.Context, env Envelope, p %s) error\n", g, ev.PayloadType))
	}
	sb.WriteString("}\n\n// DefaultHandler is a no-op Handler useful for embedding.\ntype DefaultHandler struct{}\n\n")
	for _, ev := range spec.Events {
		g := eventGoName(ev.Name)
		sb.WriteString(fmt.Sprintf("func (DefaultHandler) Handle%s(_ context.Context, _ Envelope, _ %s) error { return nil }\n", g, ev.PayloadType))
	}
	sb.WriteString("\n// Dispatch routes env to the matching Handle* method on h.\nfunc Dispatch(ctx context.Context, env Envelope, h Handler) error {\n\tswitch env.Type {\n")
	for _, ev := range spec.Events {
		g := eventGoName(ev.Name)
		sb.WriteString(fmt.Sprintf("\tcase EventType%s:\n\t\tvar p %s\n\t\tif err := json.Unmarshal(env.Payload, &p); err != nil {\n\t\t\treturn err\n\t\t}\n\t\treturn h.Handle%s(ctx, env, p)\n", g, ev.PayloadType, g))
	}
	sb.WriteString("\tdefault:\n\t\treturn fmt.Errorf(\"unknown event type %q\", env.Type)\n\t}\n}\n")
	return writeFile(path, []byte(sb.String()))
}

func publishArgs(ev EventDef) (argName, partitionExpr string, err error) {
	switch ev.Partition {
	case "runtime":
		return "runtimeID", "PartitionRuntime(runtimeID)", nil
	case "card":
		return "cardID", "PartitionCard(cardID)", nil
	case "webhook_endpoint":
		return "endpointID", "PartitionWebhook(endpointID)", nil
	case "cron_rule":
		return "ruleID", "PartitionCron(ruleID)", nil
	case "realm":
		return "realmID", "PartitionRealm(realmID)", nil
	case "actor":
		return "actorID", "PartitionActor(actorID)", nil
	default:
		return "", "", fmt.Errorf("unknown partition %q for event %s", ev.Partition, ev.Name)
	}
}

func writeGoPublish(spec *Spec, path string) error {
	var sb strings.Builder
	sb.WriteString("package eventlog\n\nimport (\n\t\"context\"\n)\n\n")
	for _, ev := range spec.Events {
		g := eventGoName(ev.Name)
		argName, partExpr, err := publishArgs(ev)
		if err != nil {
			return err
		}
		// Out-of-transaction Publish (single-event) helper. The signature
		// takes the public Log interface and routes through Log.Atomic so
		// no caller can reach for an internal append shortcut — Atomic
		// remains the only write path (§4.2 / §11.1#5). Marshalling is
		// handled inside the InTx helper via uow.Append.
		sb.WriteString(fmt.Sprintf(
			"// Publish%s appends a %s envelope (category=%s, version=%d) outside a transaction.\nfunc Publish%s(ctx context.Context, log Log, %s string, p %s, opts ...PublishOption) (int64, error) {\n"+
				"\tvar seq int64\n"+
				"\tenvs, err := log.Atomic(ctx, func(uow UnitOfWork) error {\n"+
				"\t\treturn Publish%sInTx(ctx, uow, %s, p, opts...)\n"+
				"\t})\n"+
				"\tif err != nil {\n\t\treturn 0, err\n\t}\n"+
				"\tif n := len(envs); n > 0 {\n\t\tseq = envs[n-1].Seq\n\t}\n"+
				"\treturn seq, nil\n}\n\n",
			g, ev.Name, ev.Category, ev.Version,
			g, argName, ev.PayloadType,
			g, argName,
		))
		// In-transaction Publish helper for use inside log.Atomic(...).
		sb.WriteString(fmt.Sprintf(
			"// Publish%sInTx appends a %s envelope inside an open UnitOfWork.\nfunc Publish%sInTx(ctx context.Context, uow UnitOfWork, %s string, p %s, opts ...PublishOption) error {\n"+
				"\to := collectPublishOptions(opts)\n"+
				"\treturn uow.Append(ctx, EnvelopeDraft{\n\t\tPartition: %s,\n\t\tType: EventType%s,\n\t\tVersion: %d,\n\t\tCategory: %s,\n\t\tPayload: p,\n\t\tActor: o.actor,\n\t\tTraceID: o.traceID,\n\t\tSpanID: o.spanID,\n\t})\n}\n\n",
			g, ev.Name,
			g, argName, ev.PayloadType,
			partExpr, g, ev.Version, categoryConstName(ev.Category),
		))
	}
	return writeFile(path, []byte(sb.String()))
}

func writeGoAudit(spec *Spec, path string) error {
	var sb strings.Builder
	sb.WriteString("package eventlog\n\n")
	sb.WriteString("import (\n\t\"encoding/json\"\n\t\"fmt\"\n)\n\n")
	sb.WriteString("// AuditRequiredEventTypes lists envelope.type values that require audit_summary in contracts.\n")
	sb.WriteString("var AuditRequiredEventTypes = []string{\n")
	for _, ev := range spec.Events {
		if ev.AuditRequired {
			sb.WriteString(fmt.Sprintf("\tEventType%s,\n", eventGoName(ev.Name)))
		}
	}
	sb.WriteString("}\n\n// AuditSummaryTemplate returns the raw template string from contracts or \"\".\nfunc AuditSummaryTemplate(eventType string) string {\n\tswitch eventType {\n")
	for _, ev := range spec.Events {
		if ev.AuditRequired {
			sb.WriteString(fmt.Sprintf("\tcase EventType%s:\n\t\treturn %q\n", eventGoName(ev.Name), ev.AuditSummary))
		}
	}
	sb.WriteString("\tdefault:\n\t\treturn \"\"\n\t}\n}\n\n")
	// per-event summarize<Type>(env Envelope) string that fully renders {{payload.x}} tokens.
	for _, ev := range spec.Events {
		if !ev.AuditRequired {
			continue
		}
		g := eventGoName(ev.Name)
		sb.WriteString(fmt.Sprintf("// summarize%s renders the audit_summary template for %s.\n", g, ev.Name))
		sb.WriteString(fmt.Sprintf("func summarize%s(env Envelope) string {\n", g))
		sb.WriteString(fmt.Sprintf("\tvar p %s\n", ev.PayloadType))
		sb.WriteString("\tif len(env.Payload) > 0 {\n\t\t_ = json.Unmarshal(env.Payload, &p)\n\t}\n")
		// for each field referenced in the template via {{payload.x}}
		// emit an fmt.Sprintf using the struct field
		// to keep generation simple, we pass the payload struct as a JSON map for token substitution
		sb.WriteString("\treturn renderSummaryTemplate(")
		sb.WriteString(fmt.Sprintf("%q, env.Payload)\n", ev.AuditSummary))
		sb.WriteString("}\n\n")
	}
	// Dispatcher
	sb.WriteString("// RenderAuditSummary returns the human-readable summary for an audit-required envelope.\n")
	sb.WriteString("// Returns empty string if env.Type is not audit_required.\n")
	sb.WriteString("func RenderAuditSummary(env Envelope) string {\n\tswitch env.Type {\n")
	for _, ev := range spec.Events {
		if !ev.AuditRequired {
			continue
		}
		g := eventGoName(ev.Name)
		sb.WriteString(fmt.Sprintf("\tcase EventType%s:\n\t\treturn summarize%s(env)\n", g, g))
	}
	sb.WriteString("\tdefault:\n\t\treturn \"\"\n\t}\n}\n\n")
	// Helper: lightweight {{payload.x}} renderer without a real template engine.
	sb.WriteString(`// renderSummaryTemplate substitutes {{payload.x}} tokens in tmpl using
// a JSON-decoded view of payloadJSON. Unknown keys render as "<missing>".
func renderSummaryTemplate(tmpl string, payloadJSON []byte) string {
	if tmpl == "" {
		return ""
	}
	var m map[string]any
	if len(payloadJSON) > 0 {
		_ = json.Unmarshal(payloadJSON, &m)
	}
	out := make([]byte, 0, len(tmpl))
	for i := 0; i < len(tmpl); {
		if i+1 < len(tmpl) && tmpl[i] == '{' && tmpl[i+1] == '{' {
			j := i + 2
			for j+1 < len(tmpl) && !(tmpl[j] == '}' && tmpl[j+1] == '}') {
				j++
			}
			if j+1 >= len(tmpl) {
				out = append(out, tmpl[i:]...)
				break
			}
			expr := tmpl[i+2 : j]
			// trim spaces and "payload." prefix
			start := 0
			for start < len(expr) && expr[start] == ' ' {
				start++
			}
			end := len(expr)
			for end > start && expr[end-1] == ' ' {
				end--
			}
			key := expr[start:end]
			const pfx = "payload."
			if len(key) > len(pfx) && key[:len(pfx)] == pfx {
				key = key[len(pfx):]
			}
			if v, ok := m[key]; ok {
				out = append(out, []byte(fmt.Sprintf("%v", v))...)
			} else {
				out = append(out, []byte("<missing>")...)
			}
			i = j + 2
			continue
		}
		out = append(out, tmpl[i])
		i++
	}
	return string(out)
}
`)
	return writeFile(path, []byte(sb.String()))
}
