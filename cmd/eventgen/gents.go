package main

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

func writeTSOutputs(spec *Spec, webDir string) error {
	if err := os.MkdirAll(webDir, 0o755); err != nil {
		return err
	}
	if err := writeTSEvents(spec, filepath.Join(webDir, "events.gen.ts")); err != nil {
		return err
	}
	if err := writeTSPayloads(spec, filepath.Join(webDir, "event_payloads.gen.ts")); err != nil {
		return err
	}
	return writeTSGuards(spec, filepath.Join(webDir, "event_guards.gen.ts"))
}

func writeTSFile(path string, body string) error {
	hdr := "// Code generated by eventgen. DO NOT EDIT.\n\n"
	return os.WriteFile(path, []byte(hdr+body), 0o644)
}

func tsFieldName(yamlKey string) string {
	g := goFieldName(yamlKey)
	if g == "" {
		return ""
	}
	return strings.ToLower(g[:1]) + g[1:]
}

func tsTypeForField(f FieldDef) string {
	switch f.Type {
	case "string", "timestamp":
		return "string"
	case "int64":
		return "number"
	case "int32":
		return "number"
	case "bool":
		return "boolean"
	case "float64":
		return "number"
	case "bytes":
		return "string"
	case "any":
		return "unknown"
	case "array":
		it := "string"
		if f.ItemType != "" {
			it = tsScalar(f.ItemType)
		}
		return it + "[]"
	case "map":
		vt := "unknown"
		if f.ValueType == "string" {
			vt = "string"
		}
		return "Record<string, " + vt + ">"
	default:
		return "unknown"
	}
}

func tsScalar(t string) string {
	switch t {
	case "string":
		return "string"
	case "int64", "int32", "float64":
		return "number"
	case "bool":
		return "boolean"
	default:
		return "string"
	}
}

func writeTSEvents(spec *Spec, path string) error {
	var sb strings.Builder
	sb.WriteString("import type { Actor } from \"./event_payloads.gen\";\n\n")
	sb.WriteString("export type EventType =\n")
	for i, ev := range spec.Events {
		line := fmt.Sprintf("  %q", ev.Name)
		if i < len(spec.Events)-1 {
			line += " |"
		} else {
			line += ";"
		}
		sb.WriteString(line + "\n")
	}
	sb.WriteString("\nexport interface Envelope<T extends EventType = EventType> {\n")
	sb.WriteString("  seq: number;\n  partition: string;\n  type: T;\n  version: number;\n")
	sb.WriteString("  category: string;\n  ts: string;\n  payload: unknown;\n")
	sb.WriteString("  actor?: Actor;\n  trace_id?: string;\n  span_id?: string;\n}\n")
	return writeTSFile(path, sb.String())
}

func writeTSPayloads(spec *Spec, path string) error {
	var sb strings.Builder
	sb.WriteString("export interface Actor {\n  id: string;\n  kind: string;\n  realm_id: string;\n}\n\n")
	names := make([]string, 0, len(spec.Payloads))
	for n := range spec.Payloads {
		names = append(names, n)
	}
	sort.Strings(names)
	for _, n := range names {
		p := spec.Payloads[n]
		sb.WriteString(fmt.Sprintf("export interface %s {\n", n))
		keys := make([]string, 0, len(p.Fields))
		for k := range p.Fields {
			keys = append(keys, k)
		}
		sort.Strings(keys)
		for _, k := range keys {
			f := p.Fields[k]
			opt := ""
			if !f.Required {
				opt = "?"
			}
			sb.WriteString(fmt.Sprintf("  %s%s: %s;\n", tsFieldName(k), opt, tsTypeForField(f)))
		}
		sb.WriteString("}\n\n")
	}
	return writeTSFile(path, sb.String())
}

func writeTSGuards(spec *Spec, path string) error {
	var sb strings.Builder
	sb.WriteString("import type { Envelope, EventType } from \"./events.gen\";\n\n")
	sb.WriteString("export function isEvent<T extends EventType>(env: Envelope, type: T): env is Envelope<T> {\n")
	sb.WriteString("  return env.type === type;\n}\n\n")
	sb.WriteString("export function assertNever(x: never): never {\n  throw new Error(`unexpected value: ${String(x)}`);\n}\n")
	return writeTSFile(path, sb.String())
}
